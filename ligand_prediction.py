for name, smiles in LIGANDS.items():
    yaml_content = f"""version: 1
sequences:
  - protein:
      id: A
      sequence: {PROTEIN_SEQUENCE}
      msa: {MSA_PATH}
  - ligand:
      id: B
      smiles: "{smiles}"
properties:
  - affinity:
      binder: B
"""
    path = f"/content/yamls/{name}.yaml"
    with open(path, "w") as f:
        f.write(yaml_content)
    print(f"✓ Created {path}")

